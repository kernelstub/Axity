use crate::error::Span;

#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Let,
    Class,
    New,
    SelfKw,
    Fn,
    Return,
    Print,
    While,
    If,
    Else,
    Import,
    Match,
    Case,
    Default,
    IntType,
    StringType,
    BoolType,
    ArrayKw,
    MapKw,
    TrueKw,
    FalseKw,
    Ident(String),
    IntLit(i64),
    StringLit(String),
    AndAnd,
    OrOr,
    Bang,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Colon,
    Semicolon,
    Comma,
    Arrow,
    Assign,
    Dot,
    Plus,
    Minus,
    Star,
    Slash,
    Less,
    LessEq,
    Greater,
    GreaterEq,
    EqEq,
    NotEq,
    Eof,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}

