use crate::error::Span;

#[derive(Debug, Clone, PartialEq)]
pub enum TokenKind {
    Let,
    Class,
    New,
    SelfKw,
    Fn,
    Return,
    Print,
    While,
    If,
    Else,
    Try,
    Catch,
    Throw,
    Retry,
    Do,
    For,
    In,
    Import,
    Match,
    Case,
    Default,
    IntType,
    StringType,
    FltType,
    BoolType,
    ObjType,
    BufferType,
    AnyType,
    ArrayKw,
    MapKw,
    TrueKw,
    FalseKw,
    Ident(String),
    IntLit(i64),
    FltLit(i64),
    StringLit(String),
    AndAnd,
    OrOr,
    Bang,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Colon,
    Semicolon,
    Comma,
    Arrow,
    Assign,
    Dot,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Less,
    LessEq,
    Greater,
    GreaterEq,
    EqEq,
    NotEq,
    PlusPlus,
    MinusMinus,
    BitAnd,
    BitOr,
    BitXor,
    Tilde,
    Shl,
    Shr,
    Eof,
}

#[derive(Debug, Clone)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}

